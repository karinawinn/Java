public class Main {
    public static void main() {
        double x = 34.5;
        double y = 59.6;

        // Should print x is less than y
        if (x > y) {
            System.out.println("x is greater than y");
        }
        else if (x < y) {
            System.out.println("x is less than y");
        }
        else {
            System.out.println("x is equal to y");
        };
    }
}